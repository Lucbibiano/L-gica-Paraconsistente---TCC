package Modelo;

public class ModeloDoenca {

	private int coddoenca;
	private String nomeDoenca;
	private String descricaoDoenca;
	private int status_cadastro;
	
	public int getCoddoenca() {
		return coddoenca;
	}
	
	public void setCoddoenca(int coddoenca) {
		this.coddoenca = coddoenca;
	}
	
	public String getNomeDoenca() {
		return nomeDoenca;
	}
	
	public void setNomeDoenca(String nomeDoenca) {
		this.nomeDoenca = nomeDoenca;
	}
	
	public String getDescricaoDoenca() {
		return descricaoDoenca;
	}
	
	public void setDescricaoDoenca(String descricaoDoenca) {
		this.descricaoDoenca = descricaoDoenca;
	}

	public int getStatus_cadastro() {
		return status_cadastro;
	}

	public void setStatus_cadastro(int status_cadastro) {
		this.status_cadastro = status_cadastro;
	}
	
	
	
}
